# 2D lists = a list of lists

drinks = ["coffee","tea","soda"]
dinner =["pizza","burger","puff"]
dessert =["brownie","red-velvet","black-current"]

food = [drinks,dinner,dessert]

print(food[0]) #will print first list of food list
print()
print(food[0][0]) # first element of first list
print()
print(food[0][1]) # second element of first list
print()
print(food[2][2]) # third element of second list
print()